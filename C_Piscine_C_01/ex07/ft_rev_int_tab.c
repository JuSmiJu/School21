void	ft_rev_int_tab(int *tab, int size)
{
	int i;
	int j;
	int a;
	int d[size];

	i = size - 1;
	a = i;
	j = 0;
	while (j <= a)
	{
		d[j] = tab[i];
		i--;
		j++;
	}
	i = 0;
	while (i <= a)
	{
		tab[i] = d[i];
		i++;
	}
}
