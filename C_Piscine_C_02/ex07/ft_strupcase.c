char	*ft_strupcase(char *str)
{
	int i;
	int b;

	i = 0;
	b = 'A' - 'a';
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = str[i] + b;
		}
		i++;
	}
	return (str);
}
