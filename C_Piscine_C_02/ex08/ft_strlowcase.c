char	*ft_strlowcase(char *str)
{
	int i;
	int b;

	i = 0;
	b = 'A' - 'a';
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = str[i] - b;
		}
		i++;
	}
	return (str);
}
