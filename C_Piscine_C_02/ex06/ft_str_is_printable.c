int	ft_str_is_printable(char *str)
{
	int i;
	int b;
	int c;

	i = 0;
	b = 0;
	while (str[i] != '\0')
	{
		c = (int)str[i];
		if (((c >= 0) && (c < 32)) || (c == 127))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
