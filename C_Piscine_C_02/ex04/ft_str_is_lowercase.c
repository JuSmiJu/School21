int		ft_str_is_lowercase(char *str)
{
	int i;
	int b;

	i = 0;
	b = 0;
	while (str[i] != '\0')
	{
		if ((str[i] < 'a') || (str[i] > 'z'))
		{
			b++;
		}
		i++;
	}
	if ((str[0] == '\0') || (b == 0))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
