#include <unistd.h>

void	ft_print_comb2(void)
{
	char	m[5];
	int		a;
	int		b;

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			m[0] = (a / 10 + '0');
			m[1] = (a % 10 + '0');
			m[3] = (b / 10 + '0');
			m[4] = (b % 10 + '0');
			m[2] = ' ';
			write(1, m, 5);
			if (a != 98)
			{
				write(1, ", ", 2);
			}
			b++;
		}
		a++;
	}
}
